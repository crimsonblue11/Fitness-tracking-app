package com.example.mdp_cw2.database;

/**
 * Enumerable representing the thumb up / thumb down status of an annotation.
 */
public enum LogLiked {
    /**
     * No thumb up or down - default value
     */
    UNRATED,
    /**
     * Thumb up selected
     */
    THUMB_UP,
    /**
     * Thumb down selected
     */
    THUMB_DOWN,
}
